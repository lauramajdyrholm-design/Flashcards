// Parse flashcards
let flashcards = [];
let currentIndex = 0;
let filteredCards = [];
let isAnswerVisible = false;
let reviewQueue = []; // Cards due for review
let reviewedToday = new Set(); // Track reviewed cards

// Spaced Repetition intervals (in milliseconds)
const INTERVALS = {
    difficult: 60000 * 1.5,      // 1.5 minutter
    medium: 60000 * 7,           // 7 minutter
    easy: 60000 * 60 * 12        // 12 timer
};

function parseFlashcards() {
    const lines = flashcardsData.trim().split('\n');
    flashcards = lines
        .filter(line => line.trim() && !line.startsWith('#'))
        .map((line, index) => {
            const parts = line.split('\t');
            if (parts.length >= 3) {
                const topic = parts[0].replace(/<[^>]*>/g, '').trim();
                const question = parts[1].trim();
                const answer = parts[2].trim();
                
                return {
                    id: index,
                    topic: topic,
                    question: question,
                    answer: answer,
                    nextReview: Date.now(), // Due now initially
                    reviewCount: 0,
                    lastRating: null
                };
            }
            return null;
        })
        .filter(card => card !== null);
    
    // Load saved progress from localStorage
    loadProgress();
    
    filteredCards = [...flashcards];
    updateReviewQueue();
}

function getCategories() {
    const categories = new Set();
    flashcards.forEach(card => {
        categories.add(card.topic);
    });
    return Array.from(categories).sort();
}

function populateCategoryFilter() {
    const select = document.getElementById('categoryFilter');
    const categories = getCategories();
    
    categories.forEach(category => {
        const option = document.createElement('option');
        option.value = category;
        option.textContent = category;
        select.appendChild(option);
    });
}

function cleanHTML(html) {
    // Remove &nbsp; and extra whitespace
    return html.replace(/&nbsp;/g, ' ').trim();
}

function updateReviewQueue() {
    const now = Date.now();
    reviewQueue = filteredCards
        .filter(card => card.nextReview <= now)
        .sort((a, b) => a.nextReview - b.nextReview);
    
    updateStats();
}

function updateStats() {
    const dueCards = reviewQueue.length;
    const totalCards = filteredCards.length;
    const reviewedCount = reviewedToday.size;
    
    document.getElementById('dueCount').textContent = dueCards;
    document.getElementById('reviewedCount').textContent = reviewedCount;
    document.getElementById('totalCount').textContent = totalCards;
}

function saveProgress() {
    const progress = flashcards.map(card => ({
        id: card.id,
        nextReview: card.nextReview,
        reviewCount: card.reviewCount,
        lastRating: card.lastRating
    }));
    localStorage.setItem('flashcardProgress', JSON.stringify(progress));
    localStorage.setItem('reviewedToday', JSON.stringify([...reviewedToday]));
}

function loadProgress() {
    try {
        const savedProgress = localStorage.getItem('flashcardProgress');
        const savedReviewed = localStorage.getItem('reviewedToday');
        
        if (savedProgress) {
            const progress = JSON.parse(savedProgress);
            progress.forEach(saved => {
                const card = flashcards.find(c => c.id === saved.id);
                if (card) {
                    card.nextReview = saved.nextReview;
                    card.reviewCount = saved.reviewCount;
                    card.lastRating = saved.lastRating;
                }
            });
        }
        
        if (savedReviewed) {
            reviewedToday = new Set(JSON.parse(savedReviewed));
        }
    } catch (e) {
        console.log('Could not load progress:', e);
    }
}

function rateCard(rating) {
    if (reviewQueue.length === 0) return;
    
    const card = reviewQueue[0];
    const now = Date.now();
    
    // Update card based on rating
    card.lastRating = rating;
    card.reviewCount++;
    reviewedToday.add(card.id);
    
    // Calculate next review time
    let interval = INTERVALS[rating];
    
    // Adjust interval based on review count (gradually increase)
    if (card.reviewCount > 1 && rating === 'easy') {
        interval *= Math.pow(1.5, card.reviewCount - 1);
    }
    
    card.nextReview = now + interval;
    
    // Save progress
    saveProgress();
    
    // Update queue and move to next card
    updateReviewQueue();
    
    // Show next due card or continue normally
    if (reviewQueue.length > 0) {
        const nextDueCard = reviewQueue[0];
        const nextIndex = filteredCards.findIndex(c => c.id === nextDueCard.id);
        if (nextIndex !== -1) {
            currentIndex = nextIndex;
        }
    } else {
        // No more cards due, move to next card
        nextCard();
    }
    
    displayCard(currentIndex);
}

function cleanHTML(html) {
    // Remove &nbsp; and extra whitespace
    return html.replace(/&nbsp;/g, ' ').trim();
}

function displayCard(index) {
    if (filteredCards.length === 0) {
        document.getElementById('topic').textContent = 'Ingen kort fundet';
        document.getElementById('question').innerHTML = 'Prøv at ændre dine søgekriterier';
        document.getElementById('answer').innerHTML = '';
        return;
    }

    const card = filteredCards[index];
    
    document.getElementById('topic').textContent = card.topic;
    document.getElementById('question').innerHTML = cleanHTML(card.question);
    document.getElementById('answer').innerHTML = cleanHTML(card.answer);
    
    // Update stats
    document.getElementById('cardCounter').textContent = `Kort: ${index + 1} / ${filteredCards.length}`;
    document.getElementById('categoryName').textContent = card.topic;
    
    // Update progress bar
    const progress = ((index + 1) / filteredCards.length) * 100;
    document.getElementById('progressFill').style.width = progress + '%';
    
    // Reset answer visibility
    hideAnswer();
    
    // Update navigation buttons
    document.getElementById('prevBtn').disabled = index === 0;
    document.getElementById('nextBtn').disabled = index === filteredCards.length - 1;
}

function hideAnswer() {
    isAnswerVisible = false;
    document.getElementById('answerSection').classList.remove('visible');
    document.getElementById('flipBtn').textContent = 'Vis svar';
    document.getElementById('ratingButtons').classList.remove('visible');
}

function showAnswer() {
    isAnswerVisible = true;
    document.getElementById('answerSection').classList.add('visible');
    document.getElementById('flipBtn').textContent = 'Skjul svar';
    document.getElementById('ratingButtons').classList.add('visible');
}

function toggleAnswer() {
    if (isAnswerVisible) {
        hideAnswer();
    } else {
        showAnswer();
    }
}

function nextCard() {
    if (currentIndex < filteredCards.length - 1) {
        currentIndex++;
        displayCard(currentIndex);
    }
}

function prevCard() {
    if (currentIndex > 0) {
        currentIndex--;
        displayCard(currentIndex);
    }
}

function shuffleCards() {
    for (let i = filteredCards.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [filteredCards[i], filteredCards[j]] = [filteredCards[j], filteredCards[i]];
    }
    currentIndex = 0;
    displayCard(currentIndex);
}

function filterCards() {
    const category = document.getElementById('categoryFilter').value;
    const searchText = document.getElementById('searchBox').value.toLowerCase();
    
    filteredCards = flashcards.filter(card => {
        const matchCategory = category === 'all' || card.topic === category;
        const matchSearch = searchText === '' || 
            card.topic.toLowerCase().includes(searchText) ||
            card.question.toLowerCase().includes(searchText) ||
            card.answer.toLowerCase().includes(searchText);
        
        return matchCategory && matchSearch;
    });
    
    currentIndex = 0;
    updateReviewQueue();
    displayCard(currentIndex);
}

// Event listeners
document.getElementById('flipBtn').addEventListener('click', toggleAnswer);
document.getElementById('nextBtn').addEventListener('click', nextCard);
document.getElementById('prevBtn').addEventListener('click', prevCard);
document.getElementById('shuffleBtn').addEventListener('click', shuffleCards);
document.getElementById('categoryFilter').addEventListener('change', filterCards);
document.getElementById('searchBox').addEventListener('input', filterCards);

// Rating buttons
document.querySelectorAll('.rating-btn').forEach(btn => {
    btn.addEventListener('click', (e) => {
        const rating = e.currentTarget.dataset.rating;
        rateCard(rating);
    });
});

// Keyboard navigation
document.addEventListener('keydown', (e) => {
    if (e.key === 'ArrowRight') {
        nextCard();
    } else if (e.key === 'ArrowLeft') {
        prevCard();
    } else if (e.key === ' ' || e.key === 'Enter') {
        e.preventDefault();
        toggleAnswer();
    } else if (isAnswerVisible) {
        // Numeric keys for ratings when answer is visible
        if (e.key === '1') {
            rateCard('difficult');
        } else if (e.key === '2') {
            rateCard('medium');
        } else if (e.key === '3') {
            rateCard('easy');
        }
    }
});

// Check for due cards periodically (every 30 seconds)
setInterval(() => {
    updateReviewQueue();
}, 30000);

